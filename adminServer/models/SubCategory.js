import mongoose from "mongoose";

const subcategorySchema = mongoose.Schema(
  {
    subcategory: {
      type: String,
      required: true,
    },
    description: {
      type: String,
    },
    category: {
      type: mongoose.Schema.Types.ObjectId,
      ref: "Category",
      required: true,
    },
  },
  { timestamps: true }
);

export default mongoose.model("Subcategory", subcategorySchema);
